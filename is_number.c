#include "push_swap.h"

int	is_number(char *str)
{
	int	i;

	if (!str)
		return (0);
	i = 0;
	if (str[i] == '-' || str[i] == '+')
	{
		if ((str[i + 1] >= '0' && str[i + 1] <= '9'))
			i++;
		else
			aff_error(str);
	}
	while (str[i] != '\0')
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
			return (0);
		i++;
	}
	return (1);
}
